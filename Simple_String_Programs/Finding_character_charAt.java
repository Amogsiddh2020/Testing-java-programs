package Simple_String_Programs;

public class Finding_character_charAt {

	public static void main(String[] args) {
		String s1 = "Welcome to candidjava.com";
		char c = s1.charAt(3); // index start from 0
		System.out.println("the char at index position 3 is: " + c);
		char s=s1.charAt(11);
		System.out.println("****************************************");
		System.out.println("the char at index position 6 is: "+s);

	}

}
