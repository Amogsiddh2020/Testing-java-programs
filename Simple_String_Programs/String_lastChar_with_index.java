package Simple_String_Programs;

public class String_lastChar_with_index {

	public static void main(String[] args) {
		
		 String s="String Handling Functions"; 
		   System.out.println("Given String:"+s+"\n");
		 
		   String s1="g";
		   String s2="h"; 
		   System.out.println("Occurrence of the character"+" "+s1+ "in the given string is:"+s.lastIndexOf(s1,7));
		   System.out.println("Occurrence of the character"+" "+s2+ "in the given string is:"+s.lastIndexOf(s2,10));
	}

}
