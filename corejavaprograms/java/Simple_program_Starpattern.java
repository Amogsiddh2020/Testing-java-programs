package corejavaprograms.java;

public class Simple_program_Starpattern {

	public static void main(String[] args) {
		System.out.println("The program for star pattern with no ");
		for(int i=0;i<=7;i++)
		{
			for(int j=0;j<i;j++)
			{
				System.out.print(i+" ");
			}
			System.out.println();
		}

	}

}
