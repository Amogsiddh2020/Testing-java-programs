package corejavaprograms.java;

public class Simple_program_int_to_octal {

	public static void main(String[] args) {
		System.out.println("Octal representation of 15");
		System.out.println(Integer.toOctalString(15)+"\n");
		System.out.println("octal representation of 50");
		System.out.println(Integer.toOctalString(50)+"\n");
		System.out.println("octal representation of 1");
		System.out.println(Integer.toOctalString(1));
		System.out.println("octal representatin of 81");
		System.out.println(Integer.toOctalString(81));

	}

}
