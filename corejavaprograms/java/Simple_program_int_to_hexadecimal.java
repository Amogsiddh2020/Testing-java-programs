package corejavaprograms.java;

public class Simple_program_int_to_hexadecimal {

	public static void main(String[] args) {
		System.out.println("hexadecimal representation of integer no:78" );
		System.out.println(Integer.toHexString(78)+"\n");
		System.out.println("hexadecimal representation of integer no:123");
		System.out.println(Integer.toHexString(123)+"\n");
		System.out.println("hexadecimal representation of integer no:22");
		System.out.println(Integer.toHexString(22)+"\n");
		System.out.println("hexadecimal representation of integer no:20");
		System.out.println(Integer.toHexString(20)+"\n");
		System.out.println("hexadecimal representation of integer no:500");
		System.out.println(Integer.toHexString(500));

	}

}
