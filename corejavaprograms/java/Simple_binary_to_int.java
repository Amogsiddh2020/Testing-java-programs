package corejavaprograms.java;

public class Simple_binary_to_int {

	public static void main(String[] args) {
		System.out.println("sign of given  no is:567");
		System.out.println(Integer.signum(567)+"\n");
		System.out.println("sign of given no is:-485");
		System.out.println(Integer.signum(-485)+"\n");
		System.out.println("sign of given no is:0");
		System.out.println(Integer.signum(0));

	}

}
