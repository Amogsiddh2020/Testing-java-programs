package core_java_patternsprograms;

public class PatternProg8 {

	public static void main(String[] args) {
		  int i ,j;    
	       int n = 6;    
	           for(i = n; i>0 ; i-- )    
	           {    
	                for(j = 0; j<i ; j++)    
	                   {    
	                       System.out.print(i);    
	                   }    
	                        System.out.println("");    
	           }        

	}

}
